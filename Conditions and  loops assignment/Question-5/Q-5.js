// Use the while loop to print numbers from 1 to 25 in ascending and descending order.

let num = 1;
console.log("Numbers between 1 to 25 in ascending order:");
while (num <= 25) {
  console.log(num);
  num++;
}

let num2 = 25;
console.log("Numbers between 25 to 1 in descending order:");
while (num2 >= 1) {
  console.log(num2);
  num2--;
}
