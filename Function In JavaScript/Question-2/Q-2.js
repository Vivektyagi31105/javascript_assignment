// Create a JavaScript function called generateGreeting that takes a name as an argument and returns a
// personalized greeting message. Use this function to greet three different people.

function generateGreeting(name) {
  return `Hello ${name}, how's it going?`;
}
let result1 = generateGreeting("Abhi");
let result2 = generateGreeting("Sandeep");
let result3 = generateGreeting("Shubh");

console.log(result1);
console.log(result2);
console.log(result3);
